package com.ex0120;

public class Ex03 {

	public static void main(String[] args) {
		int a=10, b=5;
		System.out.println("a<b " + (a<b));
		System.out.println("a<=b " + (a<=b));
		System.out.println("a>b " + (a>b));
		System.out.println("a>=b " + (a>=b));
		System.out.println("a==b " + (a==b));
		System.out.println("a!=b " + (a!=b));
		
		boolean c=true, d=false;
		System.out.println("c&&d "+(c&&d));
		System.out.println("c||d "+(c||d));
	}

}
